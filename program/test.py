import hid

# Используйте предоставленные VID и PID
VID = 0x1D50
PID = 0x615E

# Подключаемся к устройству
try:
    device = hid.Device(VID, PID)
except IOError:
    print("Не удалось подключиться к устройству с VID={} и PID={}".format(VID, PID))
    exit(1)

# Форматируем данные для отправки
# Здесь вы должны знать, как устройство ожидает данные. Ниже пример с RGB значениями.
red = 255
green = 0
blue = 0

# Форматируем данные в байты
data = bytes([red, green, blue])

# Отправляем данные
# В зависимости от устройства, возможно, вам потребуется добавить префикс к данным
data = b'\x00' + data

device.write(data)

# Закрываем устройство
device.close()